def generate_opf(image_map, cover_img_name, title, author):
    img_items = '    <item id="cover-img" href="images/%s" media-type="image/jpeg" properties="cover-image"/>\n' % cover_img_name
    for (_, _), info in image_map.items():
        n = info["new_name"]
        if n == cover_img_name:
            continue
        rid = "img_%s" % n.replace('.jpg', '')
        img_items += '    <item id="%s" href="images/%s" media-type="image/jpeg"/>\n' % (rid, n)
    return '''<?xml version="1.0" encoding="UTF-8"?>
<package xmlns="http://www.idpf.org/2007/opf" unique-identifier="book-id" version="3.0" xml:lang="zh-CN">
  <metadata xmlns:dc="http://purl.org/dc/elements/1.1/">
    <dc:title>%s</dc:title><dc:creator>%s</dc:creator><dc:language>zh-CN</dc:language>
    <dc:identifier id="book-id">urn:uuid:mineru-epub-%s</dc:identifier>
    <dc:date>%s</dc:date>
    <meta property="dcterms:modified">%s</meta><meta name="cover" content="cover-img"/>
  </metadata>
  <manifest>
    <item id="cover-page" href="cover.xhtml" media-type="application/xhtml+xml"/>
    <item id="content" href="content.xhtml" media-type="application/xhtml+xml"/>
    <item id="nav" href="nav.xhtml" media-type="application/xhtml+xml" properties="nav"/>
    <item id="ncx" href="toc.ncx" media-type="application/x-dtbncx+xml"/>
%s  </manifest>
  <spine toc="ncx"><itemref idref="cover-page" linear="yes"/><itemref idref="content"/></spine>
</package>''' % (html.escape(title), html.escape(author),
           datetime.now().strftime('%Y%m%d%H%M%S'),
           datetime.now().strftime('%Y-%m-%d'),
           datetime.now().strftime('%Y-%m-%dT%H:%M:%SZ'), img_items)


def generate_nav(html_content):
    chapters = re.findall(r'<h1 id="(ch\d+)">([^<]+)</h1>', html_content)
    items = "\n".join('      <li><a href="content.xhtml#%s">%s</a></li>' % (cid, cname) for cid, cname in chapters)
    return '''<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE html>
<html xmlns="http://www.w3.org/1999/xhtml" xmlns:epub="http://www.idpf.org/2007/ops" xml:lang="zh-CN">
<head><meta charset="UTF-8"/><title>目录</title></head>
<body><nav epub:type="toc" id="toc"><h1>目录</h1><ol>%s</ol></nav></body></html>''' % items


def generate_ncx(html_content, title, author):
    chapters = re.findall(r'<h1 id="(ch\d+)">([^<]+)</h1>', html_content)
    pts = []
    for i, (cid, cname) in enumerate(chapters, 1):
        pts.append('    <navPoint id="navpoint-%d" playOrder="%d">\n      <navLabel><text>%s</text></navLabel>\n      <content src="content.xhtml#%s"/>\n    </navPoint>' % (i, i, cname, cid))
    return '''<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE ncx PUBLIC "-//NISO//DTD ncx 2005-1//EN" "http://www.daisy.org/z3986/2005/ncx-2005-1.dtd">
<ncx xmlns="http://www.daisy.org/z3986/2005/ncx/" version="2005-1">
  <head><meta name="dtb:uid" content="urn:uuid:book"/><meta name="dtb:depth" content="1"/></head>
  <docTitle><text>%s</text></docTitle><docAuthor><text>%s</text></docAuthor>
  <navMap>%s</navMap></ncx>''' % (html.escape(title), html.escape(author), "\n".join(pts))


