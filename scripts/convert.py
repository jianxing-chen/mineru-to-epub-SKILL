# ── Helpers ─────────────────────────────────────────────────

def is_sentence_end(text):
    """Check if text ends with sentence-ending punctuation."""
    if not text:
        return True
    last = text.rstrip()[-1] if text.rstrip() else ''
    return last in '。！？' + '"\'' + '」』）)】〕' + '：；'  # ：； are weaker but still boundaries


def is_continuation_start(text):
    """Check if text looks like a mid-sentence continuation rather than a new paragraph."""
    if not text or not text.strip():
        return True
    t = text.strip()
    # New paragraph indicators (Chinese)
    new_para_starters = [
        '首先', '其次', '最后', '第一', '第二', '第三', '另外', '此外',
        '但是', '然而', '不过', '因此', '所以', '于是', '总之', '综上所述',
        '例如', '比如', '换句话', '也就是', '这意味', '值得注意', '需要说明',
        '当然', '显然', '事实上', '实际上', '与此同', '另一方', '相反',
        '无论如', '在此基', '从某种', '在某种', '同样', '与此', '尽管',
        '问题在', '结果是', '可以肯', '毫无疑', '众所周', '有趣的是',
        '简而言', '回到', '那么', '现在', '最终', '本章', '本节',
        '这里', '我们', '大家', '读者', '在前面', '在前文', '在前一',
    ]
    # If starts with any of these, it's a new paragraph
    for s in new_para_starters:
        if t.startswith(s):
            return False
    # If starts with quote or parenthetical (dialogue continuation)
    if t[0] in '"''「『（(':
        return True
    # If first sentence is very short (tail fragment), it's a continuation
    first_period = min((t.find(p) for p in '。！？' if p in t), default=-1)
    if first_period > 0 and first_period < 15:
        return True
    # Short text (< 30 chars) ending mid-sentence is likely continuation
    if len(t) < 30 and not is_sentence_end(t):
        return True
    return False


def merge_broken_paragraphs(parts):
    """Merge consecutive <p> elements where the first ends mid-sentence
    and the second starts as a continuation (page-break artifact)."""
    if len(parts) < 2:
        return parts
    
    result = []
    i = 0
    while i < len(parts):
        part = parts[i]
        
        # Check if this is a <p> that might need merging with the next <p>
        if part.startswith('<p>') and i + 1 < len(parts):
            next_part = parts[i + 1]
            if next_part.startswith('<p>'):
                # Strip HTML to check text content
                this_text = re.sub(r'<[^>]+>', '', part[3:-4])  # remove <p> and </p>
                next_text = re.sub(r'<[^>]+>', '', next_part[3:-4])
                
                # Merge if: this ends mid-sentence OR next starts as continuation
                if (not is_sentence_end(this_text) or is_continuation_start(next_text)):
                    # Merge: strip </p> from this and <p> from next
                    merged = part[:-4] + next_part[3:]
                    result.append(merged)
                    i += 2
                    continue
        
        result.append(part)
        i += 1
    
    return result


def load_pages(part_dirs):
    """Load all Mineru part directories. Returns [{abs_page, blocks, label}]."""
    all_pages = []
    for part_dir in part_dirs:
        json_files = glob.glob(f"{part_dir}/*_content_list_v2.json")
        if not json_files:
            raise FileNotFoundError(f"No content_list_v2.json in {part_dir}")
        with open(json_files[0], "r") as f:
            pages = json.load(f)
        offset = len(all_pages)
        for i, blocks in enumerate(pages):
            all_pages.append({"abs_page": offset + i + 1, "blocks": blocks, "label": part_dir})
    all_pages.sort(key=lambda x: x["abs_page"])
    return all_pages


def character_group(char):
    """Classify a character into a group for pattern clustering."""
    if char in CIRCLED_CHARS:
        return "circled_ideograph"
    if '\u4e00' <= char <= '\u9fff' or '\u3040' <= char <= '\u30ff':
        return "cjk"
    if char.isdigit():
        return "digit"
    if char.isalpha():
        return "latin"
    if char in '*†‡§¶#':
        return "symbol"
    return "other"


def detect_body_language(text):
    """Return {cjk, latin, digit, other} ratios for a text sample."""
    if not text:
        return {"cjk": 0, "latin": 0, "digit": 0, "other": 0}
    counts = {"cjk": 0, "latin": 0, "digit": 0, "other": 0}
    for ch in text:
        g = character_group(ch)
        if g == "cjk" or g == "circled_ideograph":
            counts["cjk"] += 1
        elif g == "latin":
            counts["latin"] += 1
        elif g == "digit":
            counts["digit"] += 1
        else:
            counts["other"] += 1
    total = max(sum(counts.values()), 1)
    return {k: round(v / total, 3) for k, v in counts.items()}


def compress_image(src, dst, max_width=1200, quality=75):
    """Compress and optionally resize an image."""
    img = Image.open(src)
    w, h = img.size
    if w > max_width:
        img = img.resize((max_width, int(h * max_width / w)), Image.LANCZOS)
    if img.mode == 'RGBA':
        bg = Image.new('RGB', img.size, (255, 255, 255))
        bg.paste(img, mask=img.split()[3])
        img = bg
    elif img.mode != 'RGB':
        img = img.convert('RGB')
    img.save(dst, 'JPEG', quality=quality, optimize=True)
    return img.size


def should_include_image(img_path):
    """Filter out tiny decorative markers."""
    try:
        img = Image.open(img_path)
        w, h = img.size
        return not (w <= 120 and h <= 120 and img_path.stat().st_size < 4000)
    except Exception:
        return False

# ── Profile mode ────────────────────────────────────────────

def profile_footnotes(all_pages):
    """
    Scan the entire book and detect footnote patterns.
    Returns a dict with detected_patterns key.
    Makes ZERO semantic assumptions — just reports observable statistics.
    """
    # ── Collect all markers ──
    marker_groups = defaultdict(list)  # char_group → [(abs_page, block_idx, item_idx, raw_text)]

    for pdata in all_pages:
        ap = pdata["abs_page"]
        for bi, b in enumerate(pdata["blocks"]):
            if b.get("type") not in ("paragraph", "title"):
                continue
            ck = "paragraph_content" if b["type"] == "paragraph" else "title_content"
            items = b.get("content", {}).get(ck, [])
            
            for ci, item in enumerate(items):
                if not isinstance(item, dict):
                    continue
                raw = item.get("content", "")
                
                if item.get("type") == "equation_inline":
                    # Parse ^{...} wrapper
                    m = re.match(r'\^\{([^}]+)\}', raw.strip())
                    inner = m.group(1) if m else raw.strip()
                    if inner and len(inner) <= 5:
                        group = character_group(inner[0])
                        ctx = "".join(it.get("content", "") for it in items if isinstance(it, dict))
                        marker_groups[group].append({
                            "abs_page": ap, "block_idx": bi, "item_idx": ci,
                            "raw": raw, "inner": inner,
                            "context": ctx[max(0, ctx.find(raw)-15):ctx.find(raw)+20] if raw in ctx else raw
                        })
                
                elif item.get("type") == "text":
                    # Text-embedded markers: look for ^{number}, circled chars, etc.
                    for m in re.finditer(r'\^\{([^}]+)\}', raw):
                        inner = m.group(1)
                        if inner and len(inner) <= 5:
                            group = character_group(inner[0])
                            marker_groups[group].append({
                                "abs_page": ap, "block_idx": bi, "item_idx": ci,
                                "raw": m.group(0), "inner": inner,
                                "context": raw[max(0,m.start()-15):m.end()+15]
                            })
                    for m in CIRCLED_RE.finditer(raw):
                        inner = m.group(0)
                        group = character_group(inner[0])
                        marker_groups[group].append({
                            "abs_page": ap, "block_idx": bi, "item_idx": ci,
                            "raw": inner, "inner": inner,
                            "context": raw[max(0,m.start()-15):m.end()+15]
                        })

    # ── Collect all body prefixes ──
    body_groups = defaultdict(list)  # char_group → [{abs_page, prefix, full_text}]

    for pdata in all_pages:
        ap = pdata["abs_page"]
        for bi, b in enumerate(pdata["blocks"]):
            if b.get("type") != "page_footnote":
                continue
            items = b.get("content", {}).get("page_footnote_content", [])
            if not items:
                continue
            
            # Strategy 1: split on equation_inline boundaries
            current_prefix = None
            current_text = []
            for item in items:
                if not isinstance(item, dict):
                    continue
                content = item.get("content", "")
                if item.get("type") == "equation_inline":
                    if current_prefix is not None:
                        full = "".join(current_text).strip()
                        group = character_group(current_prefix[0]) if current_prefix else "unknown"
                        body_groups[group].append({
                            "abs_page": ap, "prefix": current_prefix,
                            "full_text": full, "text_length": len(full)
                        })
                    m = re.match(r'\^\{([^}]+)\}', content.strip())
                    current_prefix = m.group(1) if m else content.strip()
                    current_text = []
                elif item.get("type") == "text":
                    current_text.append(content)
            # Flush last
            if current_prefix is not None:
                full = "".join(current_text).strip()
                group = character_group(current_prefix[0]) if current_prefix else "unknown"
                body_groups[group].append({
                    "abs_page": ap, "prefix": current_prefix,
                    "full_text": full, "text_length": len(full)
                })
            
            # Strategy 2: fallback for text-only blocks
            if current_prefix is None:
                text = "".join(it.get("content", "") for it in items
                              if isinstance(it, dict) and it.get("type") == "text").strip()
                if text and len(text) > 0:
                    first_char = text[0]
                    group = character_group(first_char)
                    # Extract prefix
                    if CIRCLED_RE.match(first_char):
                        prefix = CIRCLED_RE.match(text).group(0)
                    elif first_char.isdigit():
                        prefix = re.match(r'^(\d+)', text).group(1)
                    else:
                        prefix = first_char
                    body_groups[group].append({
                        "abs_page": ap, "prefix": prefix,
                        "full_text": text, "text_length": len(text)
                    })

    # ── Build pattern summary ──
    patterns = []
    all_groups = set(marker_groups.keys()) | set(body_groups.keys())

    for gid, group_name in enumerate(sorted(all_groups), 1):
        markers = marker_groups.get(group_name, [])
        bodies = body_groups.get(group_name, [])
        
        if not markers and not bodies:
            continue
        
        # Determine character set and format
        marker_chars = set()
        for m in markers:
            marker_chars.update(m["inner"])
        marker_chars = "".join(sorted(marker_chars, key=lambda c: ord(c)))
        
        # Determine format
        if all(c in CIRCLED_CHARS for c in marker_chars):
            fmt = "circled_ideograph"
        elif all(c.isdigit() for c in marker_chars):
            fmt = "superscript_digit"
        elif all(c in '*†‡§¶#' for c in marker_chars):
            fmt = "symbol"
        else:
            fmt = "mixed"
        
        # Body samples
        body_samples = [b for b in bodies if b["full_text"]]
        combined_text = " ".join(b["full_text"][:200] for b in body_samples[:20])
        lang = detect_body_language(combined_text)
        avg_len = round(sum(b["text_length"] for b in body_samples) / max(len(body_samples), 1), 1)
        
        # Context samples from markers
        ctx_samples = [m["context"] for m in markers[:10]]
        
        patterns.append({
            "id": "p%d" % gid,
            "marker_chars": marker_chars,
            "marker_format": fmt,
            "ref_count": len(markers),
            "body_count": len(bodies),
            "body_samples": [b["full_text"][:150] for b in body_samples[:6] if b["full_text"]],
            "context_samples": ctx_samples[:5],
            "body_language": lang,
            "body_avg_length": avg_len,
            "pages_with_markers": len(set(m["abs_page"] for m in markers)),
            "pages_with_bodies": len(set(b["abs_page"] for b in bodies)),
        })

    return {
        "detected_patterns": patterns,
        "total_pages": len(all_pages),
        "total_markers": sum(len(marker_groups[g]) for g in marker_groups),
        "total_bodies": sum(len(body_groups[g]) for g in body_groups),
    }


# ── Convert mode: footnote extraction ───────────────────────

def extract_footnotes(all_pages, patterns):
    """
    patterns: list of {id, marker_regex, marker_eq_regex, body_prefix_regex}
    Returns: all_refs, all_contents (both lists of tuples with pattern_id at end)
    """
    all_refs, all_contents = [], []
    refs_by_page, contents_by_page = defaultdict(list), defaultdict(list)
    
    # Pre-compile regexes
    for p in patterns:
        p["_marker_re"] = re.compile(p["marker_regex"]) if p.get("marker_regex") else None
        p["_marker_eq_re"] = re.compile(p["marker_eq_regex"]) if p.get("marker_eq_regex") else None
        p["_body_prefix_re"] = re.compile(p["body_prefix_regex"]) if p.get("body_prefix_regex") else None
    
    for pdata in all_pages:
        ap = pdata["abs_page"]
        blocks = pdata["blocks"]
        
        for pi, pat in enumerate(patterns):
            pid = pat["id"]
            
            # ── Ref extraction: equation_inline ──
            for bi, b in enumerate(blocks):
                if b.get("type") not in ("paragraph", "title"):
                    continue
                ck = "paragraph_content" if b["type"] == "paragraph" else "title_content"
                items = b.get("content", {}).get(ck, [])
                for ci, item in enumerate(items):
                    if not isinstance(item, dict) or item.get("type") != "equation_inline":
                        continue
                    raw = item.get("content", "").strip()
                    if pat["_marker_eq_re"]:
                        for m in pat["_marker_eq_re"].finditer(raw):
                            inner = m.group(1) if m.lastindex else m.group(0)
                            ctx = "".join(it.get("content", "") for it in items if isinstance(it, dict))
                            all_refs.append((ap, bi, ci, inner, inner,
                                             ctx[max(0, ctx.find(raw)-15):ctx.find(raw)+20], pid))
                            refs_by_page[ap].append(len(all_refs) - 1)
                
                # ── Ref extraction: text-embedded ──
                if any(r[0] == ap and r[1] == bi and r[6] == pid for r in all_refs):
                    continue  # already captured via equation_inline in this block
                # Scan full concatenated text (like V1) but track item-level position
                all_text = "".join(it.get("content", "") for it in items if isinstance(it, dict))
                if pat["_marker_re"]:
                    for m in pat["_marker_re"].finditer(all_text):
                        inner = m.group(1) if m.lastindex else m.group(0)
                        pos = m.start()
                        # Find which text item contains this position
                        ci = -1  # Default: text-embedded marker
                        offset = 0
                        for idx, item in enumerate(items):
                            if isinstance(item, dict) and item.get("type") == "text":
                                ic = item.get("content", "")
                                if offset <= pos < offset + len(ic):
                                    ci = idx
                                    break
                                offset += len(ic)
                        # Use -1 to signal text-embedded (distinguishes from equation_inline ci >= 0)
                        all_refs.append((ap, bi, -1, inner, inner,
                                         all_text[max(0, pos-15):m.end()+15], pid))
                        refs_by_page[ap].append(len(all_refs) - 1)
        
        # ── Content extraction ──
        for bi, b in enumerate(blocks):
            if b.get("type") != "page_footnote":
                continue
            items = b.get("content", {}).get("page_footnote_content", [])
            if not items:
                continue
            
            # Strategy: split on equation_inline boundaries
            current_num = None
            current_pid = None
            current_text = []
            
            for item in items:
                if not isinstance(item, dict):
                    continue
                content = item.get("content", "")
                
                if item.get("type") == "equation_inline":
                    if current_num is not None:
                        full_text = "".join(current_text).strip()
                        all_contents.append((ap, current_num, current_num, full_text, current_pid))
                        contents_by_page[ap].append(len(all_contents) - 1)
                    
                    m = re.match(r'\^\{([^}]+)\}', content.strip())
                    inner = m.group(1) if m else content.strip()
                    current_num = inner
                    # Find which pattern this belongs to
                    current_pid = None
                    for pat in patterns:
                        if pat["_body_prefix_re"] and pat["_body_prefix_re"].match(inner):
                            current_pid = pat["id"]
                            break
                    if current_pid is None:
                        current_pid = patterns[0]["id"] if patterns else "unknown"
                    current_text = []
                
                elif item.get("type") == "text":
                    current_text.append(content)
            
            # Flush last
            if current_num is not None:
                full_text = "".join(current_text).strip()
                all_contents.append((ap, current_num, current_num, full_text, current_pid))
                contents_by_page[ap].append(len(all_contents) - 1)
            
            # Fallback: text-only blocks
            if current_num is None:
                text = "".join(it.get("content", "") for it in items
                              if isinstance(it, dict) and it.get("type") == "text").strip()
                if text:
                    for pat in patterns:
                        if pat["_body_prefix_re"]:
                            m = pat["_body_prefix_re"].match(text)
                            if m:
                                raw = m.group(0)
                                # Multi-number splitting for digit-based patterns
                                if pat.get("multi_number_split") and raw.isdigit():
                                    parts = re.split(r'(?<=\S)\s+(?=\d+\s)', text)
                                    if len(parts) > 1:
                                        for part in parts:
                                            pm = pat["_body_prefix_re"].match(part)
                                            if pm:
                                                r = pm.group(0)
                                                all_contents.append((ap, r, r, part.strip(), pat["id"]))
                                                contents_by_page[ap].append(len(all_contents) - 1)
                                        break
                                all_contents.append((ap, raw, raw, text.strip(), pat["id"]))
                                contents_by_page[ap].append(len(all_contents) - 1)
                                break
    
    return all_refs, all_contents, dict(refs_by_page), dict(contents_by_page)


def filter_enumeration_refs(all_refs, all_pages, refs_by_page):
    """Remove circled-number refs that are enumeration markers (not footnotes).
    
    Detection: consecutive circled numbers (①②③...) at BLOCK-START positions
    across same or adjacent pages → author's enumerated list.
    
    Conservative: only removes when 2+ sequential items exist, each at position 0
    in its block. Real footnotes appear inline (mid-sentence or after punctuation),
    not at paragraph start.
    """
    # Build block-start lookup
    block_is_start = {}
    for pdata in all_pages:
        ap = pdata["abs_page"]
        for bi, b in enumerate(pdata["blocks"]):
            if b.get("type") not in ("paragraph", "title"):
                continue
            items = b.get("content",{}).get("paragraph_content",[])
            full = "".join(it.get("content","") for it in items if isinstance(it, dict))
            block_is_start[(ap, bi)] = bool(full and CIRCLED_RE.match(full[0]))
    
    # Collect all circled-at-start refs sorted by (page, block)
    circled_at_start = []
    for ri, ref in enumerate(all_refs):
        ch = ref[3]
        if CIRCLED_RE.match(ch) and len(ch) == 1:
            if block_is_start.get((ref[0], ref[1]), False):
                num = int(CIRCLE_TO_NUM.get(ch, '0'))
                circled_at_start.append((ri, ref[0], ref[1], num, ch))
    
    circled_at_start.sort(key=lambda x: (x[1], x[2]))  # by page, then block
    
    # Find runs of consecutive numbers (①②③...) within ±2 pages
    to_remove = set()
    i = 0
    while i < len(circled_at_start):
        seq = [circled_at_start[i]]
        j = i + 1
        while j < len(circled_at_start):
            prev = seq[-1]
            curr = circled_at_start[j]
            # Check: sequential numbers AND within 2 pages
            if curr[3] == prev[3] + 1 and abs(curr[1] - prev[1]) <= 2:
                seq.append(curr)
                j += 1
            else:
                break
        if len(seq) >= 2:
            for s in seq:
                to_remove.add(s[0])
        i = j
    
    if to_remove:
        for ri in to_remove:
            all_refs[ri] = (all_refs[ri][0], all_refs[ri][1], all_refs[ri][2],
                           all_refs[ri][3], all_refs[ri][4], all_refs[ri][5],
                           "_removed_")
        new_refs_by_page = defaultdict(list)
        for ri, ref in enumerate(all_refs):
            if ref[6] != "_removed_":
                new_refs_by_page[ref[0]].append(ri)
        return new_refs_by_page
    
    return refs_by_page


def match_footnotes(all_refs, all_contents, refs_by_page, contents_by_page, patterns=None):
    """Match refs to bodies by pattern_id + normalized number.
    
    For patterns with multi_number_split=True (typically digit references):
      allows sharing — multiple refs can point to the same body (ibid. pattern).
    For patterns with multi_number_split=False (typically circled notes):
      strict one-to-one matching — each body used at most once.
    """
    # Determine sharing per pattern
    sharing_allowed = {}
    if patterns:
        for p in patterns:
            sharing_allowed[p["id"]] = p.get("multi_number_split", False)
    
    refs_by_pid = defaultdict(list)
    for ri, ref in enumerate(all_refs):
        refs_by_pid[ref[6]].append(ri)
    
    matched = []
    
    for pid in refs_by_pid:
        allow_share = sharing_allowed.get(pid, False)
        content_used = set()  # one-to-one tracking
        body_consumed = {}    # sharing tracking
        
        for ri in refs_by_pid[pid]:
            ref = all_refs[ri]
            rp, norm_n = ref[0], ref[4]
            
            best = None
            for dp in [0, -1, 1, -2, 2, -3, 3, -4, 4]:
                for ci in contents_by_page.get(rp + dp, []):
                    ct = all_contents[ci]
                    if ct[2] != norm_n or ct[4] != pid:
                        continue
                    
                    if allow_share:
                        # Sharing mode: multiple refs can share same body
                        bodies_before = sum(1 for cj in contents_by_page.get(ct[0], [])
                                           if cj <= ci and all_contents[cj][2] == norm_n
                                           and all_contents[cj][4] == pid)
                        key = (ct[0], ct[2], pid)
                        consumed = body_consumed.get(key, 0)
                        if consumed <= bodies_before:
                            best = ci
                            if consumed < bodies_before:
                                body_consumed[key] = bodies_before
                            break
                    else:
                        # One-to-one: first-come-first-served
                        if ci not in content_used:
                            best = ci
                            content_used.add(ci)
                            break
                
                if best is not None:
                    break
            
            if best is not None:
                matched.append((ri, best))
    
    used_ci = {ci for _, ci in matched}
    unmatched = [ci for ci in range(len(all_contents)) if ci not in used_ci]
    
    return matched, unmatched


