def _classify_toc_line(line):
    """Classify a TOC line as 'toc-part', 'toc-section', or 'toc-chapter'.
    Works for both Chinese and English books."""
    # Chinese Part headers: 第一部分, 第二部分, ...
    if re.match(r'第[一二三四五六七八九十百]+部分', line):
        return 'toc-part'
    # English Part headers: Part I, Part II, PART ONE, ...
    if re.match(r'(Part|PART)\s+([IVX]+|\w+)', line):
        return 'toc-part'
    # Chinese section headers: 后记, 参考文献, 附录, ...
    if re.match(r'(后记|後記|参考文献|參考文獻|附录|附錄|索引)\b', line):
        return 'toc-section'
    # English section headers
    if re.match(r'(Epilogue|Afterword|Bibliography|References|Appendix|Index)\b', line, re.IGNORECASE):
        return 'toc-section'
    # Default: chapter entry
    return 'toc-chapter'


def build_html(all_pages, all_refs, all_contents, matched, chapter_ranges,
               image_map, img_placement, cover_img_name, title, author,
               skip_pages, css, footnote_config):
    """Generate content XHTML. footnote_config drives display."""
    
    ftypes = footnote_config.get("types", [])
    display_order = footnote_config.get("display_order", [t["id"] for t in ftypes])
    numbering = footnote_config.get("numbering", "per_chapter")
    
    # Build lookup: (abs_page, block_idx, item_idx or raw_char) → (global_num, text, pid)
    ref_to_fn = defaultdict(dict)  # pid → {(ap, bi, ci): (gn, text)}
    text_fn_map = defaultdict(dict)  # pid → {(ap, bi, raw): (gn, text)}
    gn_counters = defaultdict(int)  # pid → counter
    
    for ri, ci in matched:
        ref = all_refs[ri]
        ct = all_contents[ci]
        pid = ref[6]
        rp, rbi, rci, raw_n = ref[0], ref[1], ref[2], ref[3]
        ct_clean = re.sub(r'^[①②③④⑤⑥⑦⑧⑨⑩\d]+\s*', '', ct[3])
        
        gn_counters[pid] += 1
        gn = gn_counters[pid]
        
        if rci == -1:
            text_fn_map[pid][(rp, rbi, raw_n)] = (gn, ct_clean)
        else:
            ref_to_fn[pid][(rp, rbi, rci)] = (gn, ct_clean)
    
    # Build display helper per type
    type_info = {t["id"]: t for t in ftypes}
    for t in ftypes:
        t.setdefault("display_style", "bracket")
        t.setdefault("label", t["id"])
        t.setdefault("anchor_prefix", "fn_" + t["id"] + "_")
    
    def render_ref(style, gn, pid):
        """Render a footnote reference in the text."""
        ti = type_info.get(pid, {})
        prefix = ti.get("anchor_prefix", "fn_")
        if style == "circle":
            display = NUM_TO_CIRCLE.get(str(gn), "({})".format(gn))
            return '<sup><a href="#%s%d" id="%sref%d" epub:type="noteref">%s</a></sup>' % (
                prefix, gn, prefix, gn, display)
        elif style == "bracket":
            return '<sup><a href="#%s%d" id="%sref%d" epub:type="noteref">[%d]</a></sup>' % (
                prefix, gn, prefix, gn, gn)
        elif style == "asterisk":
            stars = "*" * min(gn, 3)
            return '<sup><a href="#%s%d" id="%sref%d" epub:type="noteref">%s</a></sup>' % (
                prefix, gn, prefix, gn, stars)
        else:
            return '<sup><a href="#%s%d" id="%sref%d" epub:type="noteref">[%d]</a></sup>' % (
                prefix, gn, prefix, gn, gn)
    
    def render_body(style, gn, pid):
        """Render a footnote body marker (back-link)."""
        ti = type_info.get(pid, {})
        prefix = ti.get("anchor_prefix", "fn_")
        if style == "circle":
            return NUM_TO_CIRCLE.get(str(gn), "({})".format(gn))
        elif style == "bracket":
            return "[%d]" % gn
        elif style == "asterisk":
            return "*" * min(gn, 3)
        else:
            return "[%d]" % gn
    
    # ── Build HTML ──
    parts = ['<?xml version="1.0" encoding="UTF-8"?>', '<!DOCTYPE html>',
             '<html xmlns="http://www.w3.org/1999/xhtml" xmlns:epub="http://www.idpf.org/2007/ops" xml:lang="zh-CN">',
             '<head><meta charset="UTF-8"/>', '<title>%s</title>' % html.escape(title),
             '<style>%s</style>' % css, '</head>', '<body>']
    
    # Cover
    parts.append('<div class="cover-image"><img src="images/%s" alt="封面" style="max-width:100%%;height:auto;"/></div>' % cover_img_name)
    parts.append('<div class="title-page"><h1>%s</h1><p class="author">%s 著</p></div>' % (html.escape(title), html.escape(author)))
    
    for ch_idx, (ch_name, ch_start, ch_end, is_cover) in enumerate(chapter_ranges):
        if is_cover:
            continue
        ch_id = "ch%d" % (ch_idx + 1)
        parts.append('<h1 id="%s">%s</h1>' % (ch_id, html.escape(ch_name)))
        
        # ── TOC chapter detection (language-agnostic) ──
        toc_keywords = ['目录', '目錄', 'Contents', 'Table of Contents', '目次']
        is_toc_chapter = any(kw in ch_name for kw in toc_keywords)
        
        # Fallback: content-based detection — if chapter has many short
        # lines matching chapter-number patterns, treat it as TOC
        _toc_content_check_done = False
        
        # Separate chapter footnote accumulators per type
        chapter_fns = {pid: [] for pid in [t["id"] for t in ftypes]}
        first_para_skipped = False
        
        for pdata in all_pages:
            ap = pdata["abs_page"]
            if ap < ch_start or ap > ch_end or ap in skip_pages:
                continue
            
            for bi, b in enumerate(pdata["blocks"]):
                t = b.get("type", "?")
                
                if t == "title":
                    ck = "title_content"
                    text = "".join(it.get("content", "") for it in b.get("content", {}).get(ck, [])
                                   if isinstance(it, dict))
                    clean = text.strip()
                    if clean and "CIP" not in clean:
                        # Skip if this title duplicates the chapter name
                        ch_norm = re.sub(r'\s+', '', ch_name)
                        t_norm = re.sub(r'\s+', '', clean)
                        if not (t_norm == ch_norm or (len(t_norm) >= 2 and t_norm in ch_norm)):
                            tag = "h2" if b.get("content", {}).get("level", 1) <= 1 else "h3"
                            parts.append('<%s>%s</%s>' % (tag, html.escape(clean), tag))
                
                elif t == "paragraph":
                    items = b.get("content", {}).get("paragraph_content", [])
                    if not items:
                        continue
                    para_parts = []
                    
                    for ci, item in enumerate(items):
                        if not isinstance(item, dict):
                            continue
                        
                        if item.get("type") == "equation_inline":
                            raw = item.get("content", "").strip()
                            m = re.match(r'\^\{([^}]+)\}', raw)
                            inner = m.group(1) if m else raw
                            
                            # Try each pattern type
                            matched_any = False
                            for tinfo in ftypes:
                                pid = tinfo["id"]
                                key = (ap, bi, ci)
                                if key in ref_to_fn.get(pid, {}):
                                    gn, ft = ref_to_fn[pid][key]
                                    para_parts.append(render_ref(tinfo.get("display_style", "bracket"), gn, pid))
                                    chapter_fns[pid].append((gn, ft))
                                    matched_any = True
                                    break
                            if not matched_any:
                                para_parts.append(html.escape(raw))
                        
                        elif item.get("type") == "text":
                            ic = item.get("content", "")
                            # Collect all marker positions across all types
                            markers_pos = []
                            for tinfo in ftypes:
                                pid = tinfo["id"]
                                mr = tinfo.get("_text_re")
                                if mr is None:
                                    # Build from marker_regex
                                    mrs = tinfo.get("marker_regex", "")
                                    if mrs:
                                        mr = re.compile(mrs)
                                        tinfo["_text_re"] = mr
                                if mr:
                                    for m in mr.finditer(ic):
                                        markers_pos.append((m.start(), m.end(), pid, m.group(1) if m.lastindex else m.group(0)))
                            markers_pos.sort(key=lambda x: x[0])
                            
                            if markers_pos:
                                last = 0
                                for start, end, pid, mval in markers_pos:
                                    para_parts.append(html.escape(ic[last:start]))
                                    key = (ap, bi, mval)
                                    if key in text_fn_map.get(pid, {}):
                                        gn, ft = text_fn_map[pid][key]
                                        tinfo = type_info.get(pid, {})
                                        para_parts.append(render_ref(tinfo.get("display_style", "bracket"), gn, pid))
                                        chapter_fns[pid].append((gn, ft))
                                    else:
                                        para_parts.append(html.escape(ic[start:end]))
                                    last = end
                                para_parts.append(html.escape(ic[last:]))
                            else:
                                para_parts.append(html.escape(ic))
                    
                    pt = "".join(para_parts).strip()
                    if pt:
                        if not first_para_skipped:
                            first_para_skipped = True
                            clean = re.sub(r'<[^>]+>', '', pt).strip()
                            short = ch_name.split()[-1] if ' ' in ch_name else ch_name
                            if clean == ch_name or clean == short:
                                continue
                        # ── TOC formatting: split multi-line TOC blocks ──
                        if is_toc_chapter:
                            clean_toc = re.sub(r'<[^>]+>', '', pt).strip()
                            lines = [l.strip() for l in clean_toc.split('\n') if l.strip()]
                            # Detect if this paragraph looks like multi-line TOC content
                            _has_cn_chapter = any(re.match(r'第[一二三四五六七八九十百]+章', l) for l in lines)
                            _has_en_chapter = any(re.match(r'\d+\.\s+\w', l) for l in lines)
                            if len(lines) >= 2 and (_has_cn_chapter or _has_en_chapter):
                                # Multi-line TOC block → split and tag each line
                                for line in lines:
                                    cls = _classify_toc_line(line)
                                    parts.append('<p class="%s">%s</p>' % (cls, html.escape(line)))
                            else:
                                # Single-line TOC entry
                                cls = _classify_toc_line(clean_toc)
                                parts.append('<p class="%s">%s</p>' % (cls, pt))
                        else:
                            # Not a TOC chapter → render as normal paragraph.
                            # (If a TOC went undetected, write a project-specific
                            #  post-processing script to fix the EPUB.)
                            parts.append('<p>%s</p>' % pt)
                
                elif t == "image":
                    bbox = b.get("bbox", [])
                    w, h = (bbox[2] - bbox[0]) if bbox else 0, (bbox[3] - bbox[1]) if bbox else 0
                    if w <= 100 and h <= 100:
                        continue
                    key = (ap, bi)
                    if key in img_placement:
                        info = img_placement[key]
                        parts.append('<figure><img src="images/%s" alt=""/></figure>' % info["new_name"])
        
        # ── Merge broken paragraphs (page-break artifacts) ──
        parts = merge_broken_paragraphs(parts)
        # Apply the merge result back to our mutable list reference
        # (parts is a local variable that we can rebind at chapter level)
        
        # ── Render footnote sections in display_order ──
        for pid in display_order:
            fns = chapter_fns.get(pid, [])
            if not fns:
                continue
            fns.sort(key=lambda x: x[0])
            tinfo = type_info.get(pid, {})
            label = tinfo.get("label", pid)
            style = tinfo.get("display_style", "bracket")
            prefix = tinfo.get("anchor_prefix", "fn_")
            
            parts.append('<div class="fn-section"><h3>%s</h3>' % html.escape(label))
            for gn, ft in fns:
                marker = render_body(style, gn, pid)
                parts.append('<aside id="%s%d" epub:type="footnote"><p><a href="#%sref%d">%s</a> %s</p></aside>' % (
                    prefix, gn, prefix, gn, marker, html.escape(ft)))
            parts.append('</div>')
    
    parts.append('</body></html>')
    return "\n".join(parts)


